# Set the punchline of the joke by using 10 as the binary for 2 in that variable.
types_of_people = 10
# The setup line, including 10 as a variable in a formatted string.
x = f"There are {types_of_people} types of people."
# Saving a string into a variable of the content of the string (only works, when string starts with letter).
binary = "binary"
# Saving a string in a variable as a placeholder for no reason whatsoever.
do_not = "don't"
# Formatting the previous two strings into this string.
y = f"Those who know {binary} and those who {do_not}."
# Printing the joke's setup.
print(x)
# Printing the joke's punchline.
print(y)
# Printing the setup as direct speech without quotation marks.
print(f"I said: {x}")
# Printing the punchline as direct speech with single quotes.
print(f"I also said: '{y}'")
# Setting up a boolean to be inserted into formatted string.
hilarious = False
# Creating a string to be formatted as a function (?)
joke_evaluation = "Isn't that joke so funny?! {}"
# Printing the string formatted as a function.
print(joke_evaluation.format(hilarious))
# Creating a string.
w = "This is the left side of..."
# Creating another string.
e = "a string with a right side."
# concatenating strings with "+", printing them.
print(w + e)
